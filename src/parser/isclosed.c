#include "minishell.h"

int	isclosed(char *s)
{
	size_t	i;
	char	c;

	i = 0;
	while (s[i])
	{
		if (s[i] == '\'' || s[i] == '\"')
		{
			c = s[i];
			i++;
			while (s[i] && s[i] != c)
				i++;
			if (s[i] != c)
				return (0);
		}
		i++;
	}
	return (1);
}
